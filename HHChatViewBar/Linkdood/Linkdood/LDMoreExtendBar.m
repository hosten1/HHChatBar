#import "LDMoreExtendBar.h"

@implementation LDMoreExtendBar


- (IBAction)sendBtnPressed:(id)sender {
    if ([self.delegate respondsToSelector:@selector(sendButtonPressed:)]) {
        [self.delegate sendButtonPressed:sender];
    }
}
- (IBAction)cancelBtnPressed:(id)sender {
    if ([self.delegate respondsToSelector:@selector(cancelButtonPressed:)]) {
        [self.delegate cancelButtonPressed:sender];
    }
}

@end
